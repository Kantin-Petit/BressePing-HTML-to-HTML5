club Bresse Ping, il fallait donc se faire connaître auprès de la
population Bressane.<br>
Pour cela, une <span style="font-weight: bold; text-decoration: underline;">exhibition de
Tennis de Table</span> de haut niveau a eu lieu le Samedi 26 Septembre
2009, au Gymnase du SIVOS (cité scolaire Henri Vincenot à Louhans).<br>
La "GUEST" était <span style="font-style: italic;">Carole GRUNDISCH</span>,&nbsp;
cette jeune fille de 23 ans, originaire de Port-Lesney (39), et
évoluant au sein du Club US Kremlin Bicêtre à Paris, est N°1 Française
et 85ème Mondiale. Elle a offert aux bressans présents ce jour-là -avec
beaucoup de gentillesse et de générosité-&nbsp; une remarquable
