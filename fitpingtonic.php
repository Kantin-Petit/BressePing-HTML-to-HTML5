              Le Fit Ping Tonic n'est pas une nouveauté. Sous d'autres noms (Ping Tonic, Ping Gym, Ping Fitness...), il
              a déjà trouvé sa place dans certains clubs franéais. Mais il gagne actuellement du terrain. Le concept :
              la découverte du tennis de table sous une forme ludique, en limitant les contraintes (technique, horaires,
              assiduité...) et dans le respect des fondamentaux pédagogiques (placement, exécution, respiration,
              intensité), tout en privilégiant convivialité, partage et dépense d'énergie par des exercices de remise en
              forme personnalisés. Cette activité, mixte, s'adapte à tous les niveaux de pratique, à tous les niveaux de
              sportifs et à tous les âges. Même les hommes s'y mettent !
