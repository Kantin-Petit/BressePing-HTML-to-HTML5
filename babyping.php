      <big><big><big><big><big><big>L</big></big></big></big></big></big>e baby-ping a démarré à Bresse-Ping durant la saison 2013-2014, sous
l'impulsion de notre ATD Yoann Guillemin et encadré par la Fédération,
qui a initié l'action "Premier Pas Pongiste". Cette action a amené
Yoann, ainsi que Christophe et Florian, dans les écoles maternelles de
Ratte, Bantanges, Branges, Louhans (Stella), Châteaurenaud et
Sainte-Croix afin de faire découvrir aux enfants de 4 à 7 ans notre
sport préféré. Ces demi-journées de découverte ont alors permis aux
enfants de venir&nbsp; participer aux séances encadrées du mercredi
après-midi. Florian, Patrice et Thierry leur ont ainsi pu faire
découvrir des activités ludiques autour du Ping, toujours dans la bonne
humeur! <br>
      Les séances de Baby-Ping ont continué la saison
suivante, avec une vingtaine d'enfants (le plus petit avait à peine 4
ans!). Cela a permis à Bresse-Ping de décrocher le <span style="font-weight: bold; font-style: italic;">label Ping 4-7 ans</span>, en remplissant les critères d'attribution.<br>
Cette année, une intervention de Florian et Christophe a eu lieu à la
crêche Hola Kids de Branges mardi 20 octobre, 11 enfants y ont
participé, sous l'oeil bienveillant de la directrice Emilie Vinck. <br>
