            Yoann, ainsi que Christophe et Florian, dans les écoles maternelles de Ratte, Bantanges, Branges, Louhans
            (Stella), Châteaurenaud et Sainte-Croix afin de faire découvrir aux enfants de 4 à 7 ans notre sport
            préféré. Ces demi-journées de découverte ont alors permis aux enfants de venir participer aux séances
            encadrées du mercredi après-midi. Florian, Patrice et Thierry leur ont ainsi pu faire découvrir des
            activités ludiques autour du Ping, toujours dans la bonne humeur!<br>
