                                    <p>
                                          Pour la dernière compétition de l'année en Tennis de Table, 18 comités
                                          s'étaient donnés rendez-vous ce week-end à charleville-Mézières (08) pour
                                          s'affronter. Chaque département de la zone Est avait choisi 4 joueurs et 4
                                          joueuses dans les 4 catégories d'âge (Poussin(e), benjamin(e), minime et
                                          cadet(te)), chacun des joueurs et joueuses rencontrant son homologue d'un
                                          autre département. Pour l'équipe de Saône et Loire, Flavien Heurtin de Bresse
                                          Ping avait été retenu en poussin. L'équipe s'est remarquablement comportée
                                          puisqu'elle s'est hissée jusqu'en finale, ne s'inclinant que contre l'équipe
                                          de Moselle 6 matchs à 2 (matchs de Flavien et Mathieu Deléglise). Flavien aura
                                          été impérial tout au long du week-end en remportant, avec la manière, toutes
                                          ses rencontres.
